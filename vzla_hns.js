function ipRange(start, end) {
    function ipToNumber(ip) {
        return ip.split('.').reduce((acc, octet) => (acc << 8) | parseInt(octet, 10), 0);
    }
    
    function numberToIp(num) {
        return [
            (num >>> 24) & 0xFF,
            (num >>> 16) & 0xFF,
            (num >>> 8) & 0xFF,
            num & 0xFF
        ].join('.');
    }

    const startNum = ipToNumber(start);
    const endNum = ipToNumber(end);

    const count = endNum - startNum;
    const ret = new Array(count);
    for (let i = 0; i <= count; i++) {
        ret[i] = numberToIp(i + startNum);
    }
    return ret;    
}

const dns = require('dns');
async function rdns(ip) {
    return new Promise((resolve,reject) => {
        dns.reverse(ip, (err, hns) => {
            if (err) {
                // console.error(err);
                resolve([]);
            } else {
                resolve(hns); 
            } });
    });
}


async function rdnsRange({start,end}) {
    return (await Promise.all(ipRange(start,end).map(rdns))).flat(2);
}

